export function FormatEmailList(emails, glue = ', ') {
    let value = emails.join(glue);

    if (emails.length > 3) {
        let length = emails.length;
        value = emails.slice(0, 2).join(glue);
        value += ' and ' + (length - 2) + ' more';
    }

    return value;
}
